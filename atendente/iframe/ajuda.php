<b><u>1 - INSTALAÇÃO</u></b><br />
Para efetuar a instalação do sistema em seu site, basta colar o código, que aparece no topo do painel administrador, no HTML do seu site.<br />

<br /><br />
<b><u>2 - FUNCIONAMENTO</u></b><br />
Uma vez instalado em seu site, o sistema lhe permite atender clientes de forma rápida e interativa.<br />
Sempre que houver um atendente online, aparecerá um botão de "atendimento online" em seu site.<br />
É de extrema importância clicar no botão "[ sair ]" para efetuar logout do sistema, pois sem isso, sempre aparecerá o botão de atendimento em seu site.<br />

<br />
<b><u>2.1 - ATENDIMENTOS</u></b><br />
Todas as vezes que houver um novo chamado, você ouvirá um sinal sonoro e aparecerá na lista de atendimentos, um botão vermelho com o nome da pessoa.<br />
Ao clicar nesse botão, abrirá uma janela para o atendimento ser feito. O botão que era vermelho mudará de cor para amarelo. Isso indicará que aquele atendimento está em andamento.<br />
Ao finalizar um atendimento, clique no botão "Finalizar" ao lado do nome da pessoa, o botão ficará verde.<br />
Caso o atendimento seja finalizado pelo cliente, o botão ficará laranja.<br />

<br />
<b><u>2.2 - ATENDENTES</u></b><br />
Ao se cadastrar no nosso sistema, automaticamente é gerado um atendente administrador em sua conta.<br />
Você poderá criar novos atendentes em sua conta de acordo com o plano contratado.<br />
O usuário administrador poderá ver o histórico de atendimentos, criar novos atendentes e excluí-los e modificar os dados de cadastro.<br />

<br />
<b><u>2.3 - SETORES</u></b><br />
Você, como usuáruio administrador, poderá criar setores de atendimento e excluí-los.<br />
Assim, poderá criar atendentes encarregados de setores específicos, no entanto, se o seu plano suportar apenas um atendente, não poderá fazê-lo, já que terá, no máximo, um atendente.

