<b><u>3.1 - BOTÃO DE ATENDIMENTO</u></b><br />
Para alterar o botão de atendimento que fica em seu site, basta colar a URL da nova imagem no campo de "Insira a url da imagem" no menu "Alterar botão" e salvar. Você poderá também alterar o posicionamento do botão na sua página. Você tem 5 opções de onde colocar o botão, nos 4 cantos da tela, superior direito, superior esquerdo, inferior direito, inferior esquerdo ou em qualquer outro lugar que você quiser. No último caso, é preciso colar o &lt;script&gt; no exato lugar onde você quer que ele apareça, seguindo normalmente no código HTML.<br />

